public class Field {
    /*
        1. Field/properties/attributes adalah data yang bisa
           disisipkan di dalam Object
        2. harus dideklarasikan
        3. pembuatan sama dengan variable;
     */

    /*
        MANIPULASI FIELD
        1. bisa dimanipulasi pokoknya tidak dideklarasikan final
        2. manipulasi data seperti cara pada variable
        3. mengakses field pakai keyword titik (.)
           --> namaObject.field
     */

    // Contoh field
    String nama;
    int umur;
    double nilai;
    int[] samples;

    // Field bersifat final tidak bisa dirubah
    final String kota = "Kediri";
}
